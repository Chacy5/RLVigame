def ensure_user(user_id: int):
    conn = get_db()
    cur = conn.cursor()
    cur.execute("SELECT user_id FROM users WHERE user_id = ?", (user_id,))
    row = cur.fetchone()
    if not row:
        cur.execute(
            "INSERT INTO users (user_id, coins, created_at) VALUES (?, ?, datetime('now'))",
            (user_id, 0),
        )
        conn.commit()
    conn.close()


def add_coins(user_id: int, amount: int):
    conn = get_db()
    cur = conn.cursor()
    cur.execute("UPDATE users SET coins = COALESCE(coins,0) + ? WHERE user_id = ?", (amount, user_id))
    conn.commit()
    conn.close()


def get_user_coins(user_id: int) -> int:
    conn = get_db()
    cur = conn.cursor()
    cur.execute("SELECT coins FROM users WHERE user_id = ?", (user_id,))
    row = cur.fetchone()
    conn.close()
    return int(row["coins"] if row else 0)


def reset_user(user_id: int):
    conn = get_db()
    cur = conn.cursor()
    cur.execute("DELETE FROM users WHERE user_id = ?", (user_id,))
    cur.execute("DELETE FROM main_quest_progress WHERE user_id = ?", (user_id,))
    cur.execute("DELETE FROM rewards_obtained WHERE user_id = ?", (user_id,))
    conn.commit()
    conn.close()


def mark_quest_completed(user_id: int, quest_id: str) -> Optional[MainQuest]:
    quest = MAIN_QUESTS.get(quest_id)
    if not quest:
        return None
    conn = get_db()
    cur = conn.cursor()
    cur.execute(
        """
        INSERT INTO main_quest_progress (user_id, quest_id, completed, completed_at)
        VALUES (?, ?, 1, datetime('now'))
        ON CONFLICT(user_id, quest_id) DO UPDATE SET completed = 1, completed_at = excluded.completed_at
        """,
        (user_id, quest_id),
    )
    conn.commit()
    conn.close()
    return quest


def user_quest_status(user_id: int) -> Dict[str, bool]:
    conn = get_db()
    cur = conn.cursor()
    cur.execute(
        "SELECT quest_id, completed FROM main_quest_progress WHERE user_id = ?",
        (user_id,),
    )
    rows = cur.fetchall()
    conn.close()
    return {row["quest_id"]: bool(row["completed"]) for row in rows}


def add_reward_record(user_id: int, source: str, rarity: str, lootbox_type: int, reward_text: str):
    conn = get_db()
    cur = conn.cursor()
    cur.execute(
        """
        INSERT INTO rewards_obtained (user_id, source, rarity, lootbox_type, reward_text, created_at)
        VALUES (?, ?, ?, ?, ?, datetime('now'))
        """,
        (user_id, source, rarity, lootbox_type, reward_text),
    )
    conn.commit()
    conn.close()


def get_rewards_for_user(user_id: int):
    conn = get_db()
    cur = conn.cursor()
    cur.execute(
        """
        SELECT source, rarity, lootbox_type, reward_text, created_at
        FROM rewards_obtained
        WHERE user_id = ?
        ORDER BY created_at DESC
        """,
        (user_id,),
    )
    rows = cur.fetchall()
    conn.close()
    return rows


